package org.meeuw.i18n.languages;


/**
 * The 'type' of the language as defined in ISO-639-3
 */
public enum Type {
    /**
     * Ancient language
     */
    A,
    /**
     * Constructed language
     */
    C,
    /**
     * Extinct language
     */
    E,
    /**
     * Historical language
     */
    H,
    /**
     * Living language
     */
    L,
    /**
     * Special language
     */
    S
}
